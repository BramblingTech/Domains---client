    <h2 id="What is a domain name?">What is a domain name?</h2>
    <p>A domain (or domain name) is the address of a website on the Internet. Imagine a situation: you are invited to a
      meeting at an unfamiliar restaurant. To get to your destination, you need to find out the address of the
      establishment. The same happens with sites on the network: to visit a web resource, you need to know its address,
      that is, the domain name. Look in the address bar of your browser: you will see the URL of the page you are
      currently on.</p>

    <h2 id="How Domain Names Actually Work">How Domain Names Actually Work</h2>
    <p>Each site is endowed with its own place that is called an IP address. This ensures full functioning and allows
      resources to not conflict in the operating mode. Domain names were invented to make it easier to find sites on the
      Internet. Previously, you had to remember the digital address of a website in order to open it in a browser. A
      numeric address or IP address is a combination of four numbers separated by dots. The IP address points to the
      server where the site is located, and looks like this: 7.135.10.13.</p>
    <p>The domain can contain from 2 to 63 characters. Most often, website owners prefer to register it themselves and
      then rent it. The names are sold on sites created specifically for this. On them, everyone has access to the
      function of checking for coincidence with the existing ones.</p>
    <p>You can remember a couple of these numbers, but try to keep a couple of dozen IP addresses in your head, and the
      numbers will start to get confused. Computers do not care, they will remember as many numbers as they want, but
      for people they came up with text names.</p>
    <p>By the same logic, we do not learn phone numbers by heart, but save them to the phone book. And at the right
      time, all that remains is to find the name on the phone, and the phone itself will understand which number to
      call. Any domain consists of levels. Domain levels are the parts that separate the dots. They are needed to
      organize the work of domain names.</p>
    <p>Every user dreams of getting a resounding name for their website. But its form does not in the least affect the
      attitude of the domain name system towards it. It matters only for the users who find themselves on the site. If
      the name is expressive and sonorous, it is well remembered and reflects the main theme of the resource, it is
      highly likely that visitors will like it. After all, a domain is a combination of a memorable name and a given
      topic.</p>
    <p>Their structure is based on a hierarchy in the form of a subdivision into parts. So, the names of the third
      degree are based on the names of the second, the names of the second, in turn, on the basis of the first. They are
      divided into the following types.</p>
    <p>Address: docs.google.com</p>
    <ul class="dots">
      <li>domain or first level name - .com</li>
      <li>second level domain - google.com</li>
      <li>third - docs.google.com</li>
    </ul>
    <p>The registration department is responsible for the first level. It stores information about the second level. The
      domain registrar is responsible for the second and subsequent levels (this is the company from which you bought
      the domain). This hierarchy helps the browser to quickly find the required server and open the site. By default,
      each domain consists of two levels, but there can be more of them - three, four, five, etc.
    </p>
    <h2 id="Difference between a domain name and a hosting">Difference between a domain name and a hosting</h2>
    <p>Hosting is the place where your website files are stored. And the domain is the name of the site. To make the
      site work and make it easier for people to enter it, you need both a domain and hosting. Imagine that the site is
      a collection of paintings. Then hosting is the house where the paintings are located, and the domain is the
      address where this house can be found.</p>
    <h2 id="Types of Domain Names">Types of Domain Names</h2>
    <p>Domain names are divided into two groups: general and national.</p>
    <p>Domains of different groups have different registration requirements. Let's consider the features of each
      group</p>
    <p><strong>General</strong> domains mean a specific area of activity. For instance:</p>
    <ul class="dots">
      <li>.com - for commercial enterprises, abbreviated from commercial company;</li>
      <li>.org - for non-profit organizations, short for organization;</li>
      <li>.edu - for educational enterprises, short for education.</li>
    </ul>
    <p>Over time, .com domains became so popular that not only commercial enterprises began to register them. Today,
      nobody can stop you from registering a .com domain if you are not doing business.</p>
    <p>The main feature of these domains is easy registration. Domains like .com, .net, .org, .biz and .info have no
      registration restrictions. They can be registered by any legal entity or individual. No documents are required for
      registration.</p>
    <p>Since 2011, a new category has been added to the general domains called “new general domains”. These domains are
      made up of whole words. Some of them are: .bank, .site, .shop, .website.</p>
    <p>Registration offices are constantly adding new top-level domains. Now there are over 1000 of them. So don't be
      discouraged if .com is already taken. Check the domain with a different ending, it may be free.</p>
    <h2 id="National">National</h2>
    <p>All country-level first-level domains consist of two letters. These are top level domains that are allocated to
      specific states. In most cases, country code top-level domains were based on two-letter country codes. So, .UK
      means Great Britain, .UA is Ukraine, .ME is Montenegro, etc.</p>
    <p>Some countries' top-level domains are popular around the world because they resemble common abbreviations. For
      example, Tuvalu national domains .tv are registered for TV channel sites, and Federated States of Micronesia .fm
      domains are registered for radio station sites.</p>
    <p>Registering a national domain is usually more difficult than registering a general domain. Some states only allow
      their nationals or official residents to register their country code domains. In this case, when registering, you
      will have to indicate the address within the country, passport number or driver's license.</p>
    <p>For example, when registering a French domain (.fr), you need to include your name, year and place of birth. If
      this is not done, the domain will not be registered. Different country code domains have different
      requirements.</p>
    <p>Some domains can be registered in the national language of the state. Such domains are called "internationalized"
      or "IDN domains".</p>
    <h2 id="How to Choose a Domain Name for Your Website">How to Choose a Domain Name for Your Website</h2>
    <p>Remember the purpose of the name you are choosing. It should not only attract with its convenience and sonority,
      but also contribute to the efficient functioning of the resource. It is best to use the brand name, abbreviation
      and, of course, keywords. In this way, it will be possible to make the brand and the organization itself
      recognizable, thanks to high positions in the search results.</p>
    <p>The simpler and shorter the letter combination, the easier it is for site visitors to remember it. Like, for
      example, fb.com or nytimes.com. Many site owners prefer using abbreviations, but at the same time, it is necessary
      to make sure that the abbreviation you have chosen doesn’t resemble another.</p>
    <p>For a business to develop successfully, it is important to choose the right domain name. It is not enough for the
      name to comply with the registrar's rules and be unique. For example, the domain name tastyfoodfororderingnow.com
      is unlikely to succeed, although it is possible to register it.
    </p>
    <p>We have formulated 4 rules that can be followed when choosing a good name.</p>
    <ol>
      <li>1. The domain name should be simple in spelling and perception. As a rule, domain names that are logical and
        easy to pronounce are easy to understand. They are not necessarily short. The domain "come-together.com" is
        easier to remember and write down than "cmtgtr.com". Choose domain names that are easy to read and write.
      </li>
      <li>2. The domain name corresponds to the subject matter. The topic can be indicated directly or associatively.
        The names "dazzling-diamonds.com" or "luxury-jewellery.org" carry more semantic meaning than the domain name
        "how-beautiful-everything-shines.com"
      </li>
      <li>3. The domain name is consonant with the name of the company. If a company has existed for a long time, the
        brand is famous, but it still has not had a website, it is rational to link the domain name to the brand name.
        This can be done by adding prefixes or additional explanatory words, if the name itself in the selected zone is
        already taken. It is easier for clients of the company "100 Foods" to remember "100foodsonline.com" than the
        name "tastyfoodyoucanbuy.com"
      </li>
      <li>4. The domain name is geo-referenced. The rule is suitable for local businesses like beauty salons,
        kindergartens, medical centers, offline stores, cafes. Names "london-nail-bar.com" or "oxfordkidseducation.com"
        directly indicate the geography of the business, cutting off the non-target audience and helping the user to
        better navigate the search results.
      </li>
    </ol>
